// GenerateGo produces a Go source file that defines a Language for the given grammar.
func GenerateGo(g *Grammar, packageName string) string {
	var b strings.Builder

	// Header.
	fmt.Fprintf(&b, "// Code generated by tsgo-generate; DO NOT EDIT.\n")
	fmt.Fprintf(&b, "//\n")
	fmt.Fprintf(&b, "// Grammar: %s\n", g.Name)
	fmt.Fprintf(&b, "// Symbols: %d, States: %d (%d large, %d small)\n",
		g.SymbolCount, g.StateCount, g.LargeStateCount, g.StateCount-g.LargeStateCount)
	fmt.Fprintf(&b, "package %s\n\n", packageName)
	fmt.Fprintf(&b, "import ts \"github.com/treesitter-go/treesitter\"\n\n")

	// Symbol constants.
	writeSymbolConstants(&b, g)

	// Field constants.
	if g.FieldCount > 0 {
		writeFieldConstants(&b, g)
	}

	// Language function.
	funcName := exportedName(g.Name) + "Language"
	fmt.Fprintf(&b, "// %s returns the compiled %s grammar as a Language.\n", funcName, g.Name)
	fmt.Fprintf(&b, "func %s() *ts.Language {\n", funcName)

	// Parse actions.
	writeParseActions(&b, g)

	// Large parse table.
	writeParseTable(&b, g)

	// Small parse table.
	writeSmallParseTable(&b, g)

	// Small parse table map.
	writeSmallParseTableMap(&b, g)

	// Lex modes.
	writeLexModes(&b, g)

	// Primary state IDs.
	writePrimaryStateIDs(&b, g)

	// Alias sequences.
	writeAliasSequences(&b, g)

	// Symbol metadata.
	writeSymbolMetadata(&b, g)

	// Symbol names.
	writeSymbolNames(&b, g)

	// Field maps.
	writeFieldMaps(&b, g)

	// Supertype symbols.
	writeSuperTypes(&b, g)

	// External scanner data.
	writeExternalScannerData(&b, g)

	// Build the Language struct.
	fmt.Fprintf(&b, "\treturn &ts.Language{\n")
	fmt.Fprintf(&b, "\t\tVersion:                14,\n")
	fmt.Fprintf(&b, "\t\tSymbolCount:            %d,\n", g.SymbolCount)
	fmt.Fprintf(&b, "\t\tAliasCount:             %d,\n", g.AliasCount)
	fmt.Fprintf(&b, "\t\tTokenCount:             %d,\n", g.TokenCount)
	fmt.Fprintf(&b, "\t\tExternalTokenCount:     %d,\n", g.ExternalTokenCount)
	fmt.Fprintf(&b, "\t\tStateCount:             %d,\n", g.StateCount)
	fmt.Fprintf(&b, "\t\tLargeStateCount:        %d,\n", g.LargeStateCount)
	fmt.Fprintf(&b, "\t\tProductionIDCount:      %d,\n", g.ProductionIDCount)
	fmt.Fprintf(&b, "\t\tFieldCount:             %d,\n", g.FieldCount)
	fmt.Fprintf(&b, "\t\tMaxAliasSequenceLength: %d,\n", g.MaxAliasSequenceLength)
	fmt.Fprintf(&b, "\t\tPrimaryStateIDs:        primaryStateIDs,\n")
	fmt.Fprintf(&b, "\t\tParseTable:             parseTable,\n")
	fmt.Fprintf(&b, "\t\tSmallParseTable:        smallParseTable,\n")
	fmt.Fprintf(&b, "\t\tSmallParseTableMap:     smallParseTableMap,\n")
	fmt.Fprintf(&b, "\t\tParseActions:           parseActions,\n")
	fmt.Fprintf(&b, "\t\tLexModes:               lexModes,\n")
	fmt.Fprintf(&b, "\t\tLexFn:                  tsLex,\n")
	if len(g.KeywordLexStates) > 0 {
		fmt.Fprintf(&b, "\t\tKeywordLexFn:           tsLexKeywords,\n")
		fmt.Fprintf(&b, "\t\tKeywordCaptureToken:    ts.Symbol(%d),\n", g.KeywordCaptureToken)
	}
	fmt.Fprintf(&b, "\t\tSymbolNames:            symbolNames,\n")
	fmt.Fprintf(&b, "\t\tSymbolMetadata:          symbolMetadata,\n")
	if g.FieldCount > 0 {
		fmt.Fprintf(&b, "\t\tFieldMapSlices:         fieldMapSlices,\n")
		fmt.Fprintf(&b, "\t\tFieldMapEntries:        fieldMapEntries,\n")
		fmt.Fprintf(&b, "\t\tFieldNames:             fieldNames,\n")
	}
	fmt.Fprintf(&b, "\t\tAliasSequences:         aliasSequences,\n")
	// Check for supertype symbols from metadata.
	hasSupertypes := false
	for _, m := range g.SymbolMetadata {
		if m.Supertype {
			hasSupertypes = true
			break
		}
	}
	if hasSupertypes {
		fmt.Fprintf(&b, "\t\tSupertypeSymbols:       supertypeSymbols,\n")
	}
	if len(g.ExternalScannerStates) > 0 {
		fmt.Fprintf(&b, "\t\tExternalScannerStates:  externalScannerStates,\n")
	}
	if len(g.ExternalSymbolMap) > 0 {
		fmt.Fprintf(&b, "\t\tExternalSymbolMap:      externalSymbolMap,\n")
	}
	fmt.Fprintf(&b, "\t}\n")
	fmt.Fprintf(&b, "}\n\n")

	// Character range sets (for set_contains in lex functions).
	if len(g.CharacterSets) > 0 {
		writeCharacterSets(&b, g)
		writeSetContainsFunc(&b)
	}

	// Lex function.
	writeLexFunction(&b, g, false)

	// Keyword lex function.
	if len(g.KeywordLexStates) > 0 {
		writeLexFunction(&b, g, true)
	}

	return b.String()
}

// writeSymbolConstants writes the symbol constant block.
// writeFieldConstants writes field constant block.
func writeFieldConstants(b *strings.Builder, g *Grammar) {
	fmt.Fprintf(b, "// Grammar field IDs.\n")
	fmt.Fprintf(b, "const (\n")
	for i, name := range g.FieldNames {
		if i == 0 || name == "" {
			continue
		}
		constName := "Field" + exportedName(name)
		fmt.Fprintf(b, "\t%s ts.FieldID = %d\n", constName, i)
	}
	fmt.Fprintf(b, ")\n\n")
}

// writeParseActions writes the parse actions array.
func writeParseActions(b *strings.Builder, g *Grammar) {
	fmt.Fprintf(b, "\tparseActions := []ts.ParseActionEntry{\n")
	for i, a := range g.ParseActions {
		if a.IsHeader {
			reusable := "false"
			if a.Reusable {
				reusable = "true"
			}
			fmt.Fprintf(b, "\t\t// [%d]\n", i)
			fmt.Fprintf(b, "\t\t{Type: ts.ParseActionTypeHeader, Count: %d, Reusable: %s},\n", a.Count, reusable)
		} else {
			switch a.ActionType {
			case "shift":
				if a.ShiftExtra {
					fmt.Fprintf(b, "\t\t{Type: ts.ParseActionTypeShift, ShiftExtra: true},\n")
				} else if a.ShiftRepetition {
					fmt.Fprintf(b, "\t\t{Type: ts.ParseActionTypeShift, ShiftState: %d, ShiftRepetition: true},\n", a.ShiftState)
				} else {
					fmt.Fprintf(b, "\t\t{Type: ts.ParseActionTypeShift, ShiftState: %d},\n", a.ShiftState)
				}
			case "reduce":
				s := fmt.Sprintf("\t\t{Type: ts.ParseActionTypeReduce, ReduceSymbol: %d, ReduceChildCount: %d", a.ReduceSymbol, a.ReduceChildCount)
				if a.ReduceDynPrec != 0 {
					s += fmt.Sprintf(", ReduceDynPrec: %d", a.ReduceDynPrec)
				}
				if a.ReduceProdID != 0 {
					s += fmt.Sprintf(", ReduceProdID: %d", a.ReduceProdID)
				}
				s += "},\n"
				fmt.Fprint(b, s)
			case "accept":
				fmt.Fprintf(b, "\t\t{Type: ts.ParseActionTypeAccept},\n")
			case "recover":
				fmt.Fprintf(b, "\t\t{Type: ts.ParseActionTypeRecover},\n")
			}
		}
	}
	fmt.Fprintf(b, "\t}\n\n")
}

// writeParseTable writes the large state parse table.
func writeParseTable(b *strings.Builder, g *Grammar) {
	fmt.Fprintf(b, "\tparseTable := []uint16{\n")
	for s := 0; s < g.LargeStateCount; s++ {
		fmt.Fprintf(b, "\t\t// State %d\n\t\t", s)
		for i := 0; i < g.SymbolCount; i++ {
			idx := s*g.SymbolCount + i
			if idx < len(g.ParseTable) {
				fmt.Fprintf(b, "%d, ", g.ParseTable[idx])
			}
		}
		fmt.Fprintln(b)
	}
	fmt.Fprintf(b, "\t}\n\n")
}

// writeSmallParseTable writes the small parse table.
func writeSmallParseTable(b *strings.Builder, g *Grammar) {
	fmt.Fprintf(b, "\tsmallParseTable := []uint16{\n\t\t")
	for i, v := range g.SmallParseTable {
		fmt.Fprintf(b, "%d, ", v)
		if (i+1)%20 == 0 {
			fmt.Fprintf(b, "\n\t\t")
		}
	}
	fmt.Fprintf(b, "\n\t}\n\n")
}

