// writeAliasSequences writes the alias sequences array.
func writeAliasSequences(b *strings.Builder, g *Grammar) {
	size := g.ProductionIDCount * g.MaxAliasSequenceLength
	if size == 0 {
		fmt.Fprintf(b, "\taliasSequences := make([]ts.Symbol, 0)\n\n")
		return
	}
	fmt.Fprintf(b, "\taliasSequences := make([]ts.Symbol, %d)\n", size)
	// Write non-zero entries.
	hasNonZero := false
	for i, v := range g.AliasSequences {
		if v != 0 && i < size {
			fmt.Fprintf(b, "\taliasSequences[%d] = %d\n", i, v)
			hasNonZero = true
		}
	}
	if !hasNonZero {
		fmt.Fprintf(b, "\t_ = aliasSequences\n")
	}
	fmt.Fprintln(b)
}

// writeSymbolMetadata writes the symbol metadata array.
func writeSymbolMetadata(b *strings.Builder, g *Grammar) {
	fmt.Fprintf(b, "\tsymbolMetadata := []ts.SymbolMetadata{\n")
	for i, m := range g.SymbolMetadata {
		name := ""
		if i < len(g.SymbolNames) {
			name = g.SymbolNames[i]
		}
		fmt.Fprintf(b, "\t\t{Visible: %t, Named: %t", m.Visible, m.Named)
		if m.Supertype {
			fmt.Fprintf(b, ", Supertype: true")
		}
		fmt.Fprintf(b, "}, // %d: %s\n", i, name)
	}
	fmt.Fprintf(b, "\t}\n\n")
}

// writeSymbolNames writes the symbol names array.
func writeSymbolNames(b *strings.Builder, g *Grammar) {
	fmt.Fprintf(b, "\tsymbolNames := []string{\n")
	for i, name := range g.SymbolNames {
		fmt.Fprintf(b, "\t\t%q, // %d\n", name, i)
	}
	fmt.Fprintf(b, "\t}\n\n")
}

// writeFieldMaps writes the field map slices and entries.
func writeFieldMaps(b *strings.Builder, g *Grammar) {
	if g.FieldCount == 0 {
		return
	}

	fmt.Fprintf(b, "\tfieldMapSlices := []ts.FieldMapSlice{\n")
	for _, s := range g.FieldMapSlices {
		fmt.Fprintf(b, "\t\t{Index: %d, Length: %d},\n", s.Index, s.Length)
	}
	fmt.Fprintf(b, "\t}\n\n")

	fmt.Fprintf(b, "\tfieldMapEntries := []ts.FieldMapEntry{\n")
	for _, e := range g.FieldMapEntries {
		if e.Inherited {
			fmt.Fprintf(b, "\t\t{FieldID: %d, ChildIndex: %d, Inherited: true},\n", e.FieldID, e.ChildIndex)
		} else {
			fmt.Fprintf(b, "\t\t{FieldID: %d, ChildIndex: %d},\n", e.FieldID, e.ChildIndex)
		}
	}
	fmt.Fprintf(b, "\t}\n\n")

	fmt.Fprintf(b, "\tfieldNames := []string{\n")
	for _, name := range g.FieldNames {
		fmt.Fprintf(b, "\t\t%q,\n", name)
	}
	fmt.Fprintf(b, "\t}\n\n")
}

// writeSuperTypes writes the supertype symbols array.
func writeSuperTypes(b *strings.Builder, g *Grammar) {
	// Find supertype symbols from metadata.
	var supertypes []int
	for i, m := range g.SymbolMetadata {
		if m.Supertype {
			supertypes = append(supertypes, i)
		}
	}
	if len(supertypes) == 0 {
		return
	}
	fmt.Fprintf(b, "\tsupertypeSymbols := []ts.Symbol{")
	for _, s := range supertypes {
		fmt.Fprintf(b, "%d, ", s)
	}
	fmt.Fprintf(b, "}\n\n")
}

